/// \mainpage Thumb
///
/// Thumb is a distributed real-time rendering engine, implemented as a C++
/// class library. It provides facilities to render 3D content on multiple
/// networked hosts simultaneously while maintaining tight synchronization of
/// display updates at high frame rates. Thumb defines a very flexible cluster
/// configuration mechanism and supports a wide variety of stereo display
/// technologies and input devices. This gives broad application portability
/// supporting high-resolution tiled displays, immersive virtual reality
/// environments, and full dome installations, as well as common PC desktops.
///
/// Thumb provides a high-performance static geometry optimizer with support
/// for programmable materials. It links the Open Dynamics Engine to a simple
/// 3D scene graph, giving physically responsive interaction. It implements a
/// basic 3D GUI with high-quality internationalized text rendering and a
/// variety of common widgets. Bringing all of these features together, Thumb
/// implements a built-in real-time 3D scene editor with unlimited undo that
/// naturally embeds within all applications.
///
